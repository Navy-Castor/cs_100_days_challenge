package the_basics;

public class Variables {
    public static void main(String[] args) {

        //Class Name --> PascalCase
        //Variable Name --> camelCase


        //Variable Initialization
        //Variable name must not be the same with Keywords.
        //DataType   VariableName   AssignmentOperator(=)   Value   EndOperator(;)
        String facebookPageName = "CodeWall Technologies";
        Integer followerCount = 49000;

        //Ctrl + D
        System.out.println(facebookPageName);
        System.out.println(facebookPageName);

        //Modify
        facebookPageName = "CodeWall Technologies EDU";
        System.out.println(facebookPageName);
        System.out.println(facebookPageName);

        facebookPageName = facebookPageName + " Myanmar";
        System.out.println(facebookPageName);

        System.out.println(followerCount);

        followerCount = 50000;
        System.out.println(followerCount);
        followerCount = followerCount + 1000;
        System.out.println(followerCount);

        //Variable Declaration
        String themeColor;

        var name = "Mg Mg";
        var count = 7000;

        //var color;

    }


}
